package class3;

public class E6Modulus {

    public static void main(String[] args) {
        int number1=9; //odd number because the reminder is not 0
        /*
        if we divide something by 2 and reminder is zero it means the number is an even number
        and if remainder is not
        zero it means the number is odd
         */
        System.out.println(number1%2);
    }
}
