package class4;

public class E1IfElseStatement {
    public static void main(String[] args) {
        //we can use Ctrl+alt+l to format the code
        // we can click on the starting curly bracket and intellij will highlight the ending one

        boolean hungry = true;
        // if boolean is true java will execute the if part
        // otherwise will execute the else part (false)

        if (hungry) {
            System.out.println("Lets cook something");
            System.out.println("Lets eat something");
        } else {
            System.out.println("Lets go for walk");

        }
    }
}
